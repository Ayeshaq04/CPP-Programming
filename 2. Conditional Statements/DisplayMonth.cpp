#include <iostream>
using namespace std;

int main() 
{
    int mon;
    cout<<"Enter Month Number: ";
    cin>>mon;
    if(mon==1){
        cout<<"Jan"<<endl;
    }
    else if(mon==2){
        cout<<"Feb"<<endl;
    }
    else if(mon==3){
        cout<<"Mar"<<endl;
    }
    else if(mon==4){
        cout<<"Apr"<<endl;
    }
    else if(mon==5){
        cout<<"May"<<endl;
    }
    else if(mon==6){
        cout<<"Jun"<<endl;
    }
    else if(mon==7) {
        cout<<"Jul"<<endl;
    }
    else if(mon==8) {
        cout<<"Aug"<<endl;
    }
    else if(mon==9) {
        cout<<"Sept"<<endl;
    }
    else if(mon==10) {
        cout<<"Oct"<<endl;
    }
    else if(mon==11) {
        cout<<"Nov"<<endl;
    }
    else {
        cout<<"Dec"<<endl;
    }


    return 0;
}
